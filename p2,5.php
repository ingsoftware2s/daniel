<h2>Kayn</h2>
<h3>Historia</h3>
<p>Noxiano de nacimiento, Shieda Kayn y otros como él fueron reclutados como niños soldados, una práctica cruel utilizada solo por los <br>
comandantes más retorcidos del imperio de Boram Darkwill. Tras la desastrosa batalla en el Placidium de Navori, la invasión fue reformulada<br>
 deliberadamente para ser una prolongada guerra de agotamiento. La compasión de Jonia era una debilidad a explotar; sus guerreros dudarían<br>
 antes de abatir a un presunto inocente. Por lo tanto, apenas capaz de levantar la espada que le habían otorgado, el primer día de Kayn en<br>
 la batalla debía ser su último.</p>

<br><p>Atacando a la provincia de Bahrl, las fuerzas noxianas desembarcaron en la boca del río Epool. Kayn y los demás conformaban una<br>
 vanguardia renuente y se enfrentaban a bandas desorganizadas de lugareños que defendían su hogar de los conocidos invasores. Mientras sus<br>
 jóvenes compañeros eran abatidos o huían del campo de batalla, Kayn no demostró temor. Dejó caer su pesada espada y recogió una hoz caída<br>
 para enfrentarse a los sorprendidos jonios mientras los noxianos atacaban desde el costado.</p>
 
