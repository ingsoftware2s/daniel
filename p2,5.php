<br><p>La masacre fue abrumadora. Granjeros, cazadores, incluso algunos vastayanos... todos fueron descuartizados sin ceremonia.</p>

<br><p>Dos días después, una vez que se corrió la voz por los territorios sureños, la Orden de la Sombra llegó hasta esta macabra escena.<br>
 Su líder, Zed, sabía que esta área no tenía importancia táctica. La masacre debía ser un mensaje. Noxus no mostraría misericordia.</p>

<br><p>Un titilante destello de acero capturó su mirada. Un chico de no más de diez años yacía en el barro, elevando su hoz rota hacia el <br>
maestro asesino, los nudillos sangrientos casi blancos. Los ojos del chico albergaban un dolor que contradecía su edad y, a pesar de ello,<br>
 ardían con toda la furia de un guerrero curtido. Su tenacidad no era algo que se podía enseñar. Zed vio en este chico, este sobreviviente<br>
 noxiano abandonado, un arma que podría rebelarse contra aquellos que lo enviaron allí a morir. El asesino le tendió una mano y recibió a<br>
 Kayn en la Orden de la Sombra.</p>

<br><p>Tradicionalmente, los aprendices pasaban años entrenando con una sola arma de su elección, pero Kayn las dominaba todas, para él eran<br>
 simples herramientas y él era el arma. Veía la armadura como una carga engorrosa, prefería arroparse en las sombras y matar a sus<br>
 enemigos con rapidez y sigilo. Estas ágiles ejecuciones infundieron miedo en los corazones de aquellos con la suficiente fortuna para<br>
 ser perdonados.</p>

<br><p>A medida que crecía la leyenda de Kayn, también lo hacía su arrogancia. Creía que en verdad un día su poder eclipsaría incluso al de Zed.</p>

<br><p>Esta soberbia llevó a Kayn a aceptar su última prueba: buscar un arma darkin recientemente desenterrada en Noxus e impedir que fuera<br>
 utilizada en contra de los defensores agotados de Jonia. Aceptó sin dudarlo y sin cuestionar por qué había sido escogido para esa tarea.<br>
 En efecto, si bien cualquier otro aprendiz hubiera destruido la guadaña viva conocida como Rhaast, Kayn la tomó para él.</p>
