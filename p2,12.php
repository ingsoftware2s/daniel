<br><P>Durante el reinado del emperador Azir, llegaron rumores de una mística criatura de fuego que había escapado del sarcófago mágico que<br>
 la mantenía apresada en su mazmorra subterránea. Había arrasado una aldea shurimana antes de huir a través del desierto en dirección este.<br>
 Renekton y su hermano Nasus emprendieron la búsqueda de este legendario oponente. En su ausencia, el joven emperador, instado y manipulado<br>
 por su mago Xerath, intentó unirse a sus filas y convertirse en un Ascendido. Las consecuencias fueron catastróficas.</P>

<br><P>Renekton y Nasus se encontraban a una jornada de la capital, pero aun así llegaron a sentir la onda expansiva del trágico ritual <br>
de Ascensión. Sabedores de que algo terrible había ocurrido, regresaron a toda velocidad a una gloriosa ciudad en ruinas. Azir había sido<br>
 destruido, junto a la mayoría de los habitantes de la urbe, y el gran disco solar, vaciado de toda energía, caía inexorablemente. En el <br>
 epicentro de aquella catástrofe encontraron a Xerath convertido en un ser de pura energía malévola.</P>

<br><P>Los hermanos intentaron sellar a Xerath en el sarcófago mágico que había encerrado a la ancestral criatura de fuego. Lucharon <br>
durante un día y una noche, pero el mago era un rival poderoso, y no se dejaría encerrar. Logró destruir el sarcófago y asaltó a los <br>
hermanos con hechizos alimentados por el poder del disco solar, que chocó contra el suelo mientras luchaban.</P>

<br><P>Consciente de que no podrían destruir a Xerath, Renekton logró empujarlo hasta las profundidades de la Tumba de los Emperadores.<br> 
Una vez allí, rogó a su hermano que sellara por siempre aquel mausoleo con ellos dentro. Sabedor de que no habría otra forma de detener <br>
a Xerath, Nasus cumplió a regañadientes las órdenes de su hermano. Y así, mientras Renekton y Xerath sucumbían a la oscuridad, Nasus cerró<br>
 la tumba para siempre.</P>

<br><P>Entre las tinieblas, Xerath y Renekton continuaron su batalla. Lucharon durante años interminables, mientras fuera la otrora gran <br>
civilización de Shurima se venía abajo. En pleno combate, Xerath envenenó con palabras los oídos de Renekton, y lentamente, a medida que<br>
 avanzaban los siglos, aquel tóxico discurso y la omnipresente oscuridad hicieron mella en el héroe. El mago hizo creer a Renekton que <br>
 Nasus lo había sepultado a propósito, celoso de su éxito e incapaz de compartir su Ascensión.</P>

<br><P>Poco a poco, la cordura de Renekton se resquebrajó por completo. Xerath no hizo más que ahondar aquellas grietas, corrompiendo su<br>
 mente y tergiversando su visión de lo real y lo imaginario.</P>

<br><P>Miles de años después, el mercenario Sivir reabrió la Tumba de los Emperadores y liberó tanto a Renekton como a Xerath. Renekton <br>
rugió iracundo y se precipitó con estruendo hacia las arenas del desierto shurimano, olfateando el aire en busca de su hermano.</P>

<br><P>Ahora vaga por el desierto con un único objetivo: acabar con Nasus, el traidor que lo abandonó a una muerte segura. A pesar de <br>
su visión distorsionada de la realidad, en ocasiones recuerda al orgulloso y honorable héroe que fue. Sin embargo, la mayoría del tiempo<br>
 no es más que una bestia enloquecida por el odio e impulsada por su sed de sangre y venganza.</P>

