<h2>Renekton</h2>
<h3>Historia</h3>
<br><P>Renekton es una terrorífica criatura Ascendida movida por la ira y procedente de los desiertos abrasadores de <br>
Shurima. En su día fue el guerrero más admirado del imperio, un líder que condujo a los ejércitos shurimanos a incontables <br>
victorias. Sin embargo, tras la caída del imperio, Renekton quedó sepultado bajo las arenas, donde lentamente sucumbió a <br>
la locura mientras el mundo seguía girando y cambiando. Ahora, libre de nuevo, le carcome el ansia de hallar a su hermano <br>
Nasus y acabar con él, pues lo culpa en su locura de los siglos pasados entre las tinieblas.</P>

<br><P>Renekton nació para luchar. Ya desde joven se vio siempre envuelto en violentas peleas. No conocía el miedo, y era capaz<br>
 de hacer frente a niños mucho mayores que él. A menudo era el orgullo lo que provocaba estos enfrentamientos, pues Renekton era<br>
 incapaz de recular o pasar una ofensa por alto. Cada tarde volvía a casa con cortes y magulladuras, y aunque Nasus, su estudioso<br>
 hermano mayor, no veía sus peleas callejeras con buenos ojos, Renekton las disfrutaba.</P>

<br><P>Tras su admisión en el exclusivo Collegium del Sol, Nasus se marchó de casa. En sus años de ausencia, las peleas de Renekton<br>
 fueron volviéndose cada vez más serias. En uno de sus pocas visitas, Nasus quedó horrorizado ante la visión de su hermano <br>
 ensangrentado tras otra de sus riñas callejeras. Temeroso de que el carácter agresivo de Renekton lo condenara a prisión o a una<br>
 muerte prematura, Nasus ayudó a su hermano a alistarse en el ejército shurimano. Aunque Renekton era demasiado joven para incorporarse<br>
 a filas, las influencias de su hermano mayor ayudaron a pasar este detalle por alto.</P>

<br><P>La férrea disciplina del ejército fue una bendición para Renekton. En pocos años ascendió hasta convertirse en uno de los líderes<br>
 militares más capaces y temidos de Shurima, y ayudó a extender los dominios del imperio luchando en primera fila en numerosas guerras<br>
 de conquista. Pronto se granjeó fama de duro y feroz, pero también de honrado y valiente. A su vez, Nasus se convirtió en un general<br>
 condecorado, y juntos combatieron en numerosas campañas, siempre unidos a pesar de sus diferencias y de sus frecuentes desacuerdos.<br>
 El talento de Nasus residía en su capacidad para la estrategia, la logística y la historia; el de Renekton, en el combate. Nasus planificaba <br>
 las guerras y Renekton las ganaba.</P>

<br><P>Renekton alcanzó el título de Guardián de Shurima tras librar una cruenta batalla en uno de los acantilados fronterizos de la región.<br>
 Una fuerza invasora había desembarcado en la costa sur para atacar la aislada ciudad de Zuretta. Si no lograban detener la invasión, la <br>
 ciudad sería arrasada y su población, masacrada. Con una inferioridad de diez hombres contra uno, Renekton lideró un pequeño contingente <br>
 para hacer frente a los invasores, dispuesto a retrasar el ataque para evacuar la ciudad. Nadie esperaba que Renekton saliera de aquella <br>
 batalla con vida, y mucho menos con una victoria. Pero él se mantuvo firme en el desfiladero durante un día y una noche, tiempo suficiente<br>
 para que llegaran las tropas de relevo lideradas por Nasus. Con apenas un puñado de guerreros supervivientes, todos ellos libres de heridas,<br>
 Renekton fue aclamado como un héroe.</P>

<br><P>Renekton luchó en el frente durante décadas y jamás perdió una batalla. Su presencia inspiraba a aquellos que luchaban junto a él y<br>
 aterrorizaba a sus enemigos. Se alzó con una victoria tras otra, y era tal su reputación que llegó a ganar batallas sin desenvainar la espada; <br>
 naciones enemigas se rendían al saberse atacadas por un ejército liderado por Renekton.</P>

<br><P>Renekton, entrado en años, era ya un veterano canoso y curtido en mil batallas cuando le llegaron noticias de que su hermano estaba a <br>
punto de morir. Corrió de vuelta a la capital, donde descubrió a Nasus abatido por una cruel enfermedad debilitante, una sombra del imponente <br>
héroe que había sido. La enfermedad no tenía cura, y se asemejaba a una maldición degenerativa que, según decían, había acabado con todo un <br>
noble linaje en la antigüedad.</P>

<br><P>No obstante, todos reconocían la grandeza de Nasus. Además de ser un general altamente condecorado, había ejercido de curador de la gran<br>
 biblioteca de Shurima, y escrito algunas de las mayores obras literarias del imperio. El sacerdocio declaró que era la voluntad del Sol que el <br>
 héroe se sometiera al ritual de Ascensión.</P>

 
<br><P>La ciudad entera se reunió para ser testigo del sagrado ritual, pero la trágica enfermedad había causado ya sus terribles estragos y<br>
 Nasus carecía de la fuerza suficiente para subir los escalones del Estrado de la Ascensión. En un acto de amor y sacrificio extremos, <br>
 Renekton alzó a su hermano en brazos y subió los últimos peldaños, convencido de que aquel gesto lo conduciría a la muerte, debido a las <br>
 energías sagradas del disco solar. Lo consideraba un sacrificio menor si con él garantizaba que la leyenda de su hermano perdurase. No en <br>
 vano era solo un guerrero, si bien talentoso, mientras que su hermano era un erudito, pensador y general sin igual. Renekton sabía que Shurima<br>
 necesitaría a Nasus en los años venideros.</P>

<br><P>Sin embargo, Renekton no fue destruido. Bajo el cegador resplandor del disco solar, ambos hermanos fueron elevados y transformados. <br>
Cuando la luz se desvaneció, dos poderosas criaturas Ascendidas se aparecieron ante los espectadores; Nasus en su esbelto cuerpo con cabeza <br>
de chacal, y Renekton con su imponente forma de cocodrilo. Sus cuerpos eran apropiados: el chacal a menudo se consideraba una de las bestias<br>
 más inteligentes y astutas, mientras que la agresividad intrépida del cocodrilo le quedaba a Renekton como un guante. Shurima dio gracias a<br>
 los poderes divinos por aquellos nuevos semidioses y guardianes del imperio.</P>

<br><P>Renekton había sido un portentoso héroe de guerra, pero ahora era un ser Ascendido dotado de un poder incomprensible para el común de<br> 
los mortales. Poseía mayor fuerza y velocidad que cualquier humano y parecía prácticamente inmune al dolor. Aunque los seres Ascendidos no <br>
eran inmortales, su esperanza de vida era mucho mayor de lo normal, lo que les permitía servir al imperio durante cientos de años.</P>

<br><P>Con Renekton al frente de los ejércitos de Shurima, el poderío militar del imperio era imparable. Siempre había sido un guerrero feroz <br>
y un comandante despiadado, pero su nueva forma le otorgaba un poder inimaginable. Lideró a los soldados shurimanos en incontables victorias <br>
sangrientas, y jamás tuvo ni esperó clemencia. Su leyenda se extendió más allá de los confines del imperio, y fueron sus enemigos los que lo <br>
apodaron el Carnicero de las Arenas, sobrenombre que aceptó gustoso.</P>

<br><P>Había quien pensaba que una parte de la humanidad de Renekton se había perdido en su transformación, y Nasus compartía esta opinión. <br>
A medida que pasaban los años, parecía aumentar su crueldad; su sed de sangre alcanzaba cotas antinaturales, y se rumoreaba que cometía <br>
verdaderas atrocidades en nombre de la guerra. Aun así, era un fiero defensor de Shurima, y sirvió con extrema lealtad a una sucesión de <br>
emperadores, lo que permitió garantizar la seguridad y grandeza del imperio durante siglos.</P>
