<h2>Lucian</h2>
<h3>Historia</h3>
<p>lucian usa armas antiguas imbuidas en el poder del pasado, y se mantiene firme como guardián contra los no muertos. Su sangre<br>
 fría nunca flaquea, incluso ante el rostro de los exasperantes horrores que destruye con su llama de fuego purificador. Lucian<br>
 vaga solo en una cruzada nefasta: purgar los espíritus de aquellos que quedaron atrapados en la no muerte, su ser querido entre ellos.</p>

<br><p>Al igual que las armas antiguas que lleva, Lucian y su esposa Senna fueron esculpidos de la misma piedra. Juntos combatieron el mal en<br> 
Runaterra durante años, llevando la luz a la oscuridad y acabando con aquellos asediados por la corrupción. Eran los estandartes de la <br>
justicia: la dedicación de Senna a la causa nunca titubeó, mientras que la amabilidad y el confort de Lucian animaba las vidas de aquellos<br>
 que habían salvado. Dos partes para un todo, devotos e inseparables.</p>
 
