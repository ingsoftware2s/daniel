<h2>Leona</h2>
<h3>Historia</h3>
<br><P>Leona es una guerrera imbuida con fuego solar, templaria de los Solari, que defiende Monte Targón<br>
 con la Espada del Cénit y el Escudo del Amanecer. Su piel resplandece con el brillo de una estrella, <br>
 mientras sus ojos arden con el poder del aspecto celestial que lleva dentro. Leona, que viste una armadura<br>
 de oro y soporta una terrible carga de conocimiento ancestral, lleva iluminación a algunos y la muerte a<br>
 otros.</P>

<br><P>Vivir en las tierras que rodean a la imponente cumbre del Monte Targón significa aceptar una vida <br>
de dificultades. Aquellos que lo intentan por voluntad propia son testamento del poder del espíritu humano<br>
 para soportar lo que sea en búsqueda de significado y un fin superior. Tan duras como las rústicas faldas<br>
 de la base de la montaña, nada se compara con las penurias que soportan quienes moran en la propia montaña.</P>

<br><P>La vida en las alturas de Targón está plagada de peligros. Cuando desciende la niebla resplandeciente<br>
 que envuelve la cima, nunca llega sola. Al retirarse, deja a su paso todo tipo de cosas de otros mundos; <br>
 criaturas radiantes que matan al azar y voces murmurantes que susurran secretos irreproducibles para volver<br>
 locos a los mortales.</P>

<br><P>La tribu de los Rakkor, que sobrevive de plantas silvestres de la montaña y sus preciosos rebaños,<br>
 habita en los límites de la resistencia humana mientras perfecciona sus habilidades guerreras para luchar <br>
 en la guerra del fin del mundo. Rakkor significa Tribu del último sol, y su pueblo cree que existieron muchos<br>
 mundos antes de este, cada uno de los cuales había sido destruido por una gran catástrofe. Sus videntes les<br>
 enseñan que si este sol es destruido, ya no surgirá otro, por lo cual los guerreros deben prepararse para <br>
 luchar contra los que busquen extinguir su luz.</P>

<br><P>Para los Rakkor, la batalla es un acto de devoción, una ofrenda que mantiene brillando a la luz del sol. <br>
Se espera que todos los miembros de la tribu combatan y maten a sus enemigos sin misericordia o vacilación, y <br>
Leona no era la excepción. Aprendió a pelear tan pronto como pudo caminar, y dominó el uso de la espada y el <br>
escudo con gran facilidad. A Leona le fascinaban las nieblas que envolvían la cumbre y con frecuencia se preguntaba<br>
 qué habría más allá de ellas. Aquella fascinación no le impidió seguir peleando contra las feroces bestias, <br>
 entidades inhumanas y pálidos extraños sin ojos que bajaban de la montaña.</P>

<br><P>Peleaba contra ellos y los mataba tal como le habían enseñado hasta el día en que la joven Leona encontró<br>
 a un niño de piel dorada y alas de murciélago vagando por las laderas de la montaña. No hablaba su idioma, pero<br>
 no había duda que estaba perdido y asustado. Su piel resplandecía con una suave luz, y a pesar de que sabía por<br>
 sus enseñanzas que debía atacarlo, Leona no pudo permitirse asesinar a alguien tan indefenso. En su lugar, condujo<br>
 al niño por un camino que llevaba hasta la cumbre, y lo observaba mientras ascendía hacia un rayo de luz solar, y <br>
 luego, desapareció.</P>
