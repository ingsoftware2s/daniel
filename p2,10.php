<br><P>Cuando regresó con los Rakkor, la acusaron de haber fallado en su deber hacia el sol. Un muchacho llamado <br>
Atreus la había visto conduciendo a la criatura hacia la segura montaña en lugar de asesinarla. Atreus le había <br>
contado a su padre lo que Leona había hecho y este la denunció como hereje por contradecir las creencias de su <br>
pueblo. Leona no lo negó, y las leyes de los Rakkor solo admitían una sentencia para tal transgresión: juicio por <br>
combate. Leona enfrentaría a Atreus en la arena de combate bajo el sol del mediodía, y el juicio se realizaría bajo<br>
 su luz. Leona y Atreus eran rivales equivalentes; sus habilidades como guerrera eran formidables, pero Atreus siempre<br>
 había perseguido la excelencia marcial. Leona tomó su espada y escudo, Atreus su larga lanza, y ninguno de los <br>
 espectadores de la arena podrían predecir el resultado de la batalla.</P>

<br><P>Leona y Atreus combatieron bajo el sol ardiente, y aunque ambos sangraban profusamente por decenas de heridas,<br>
 ninguno lanzaba el golpe mortal. Al ocultarse el sol en el horizonte, un anciano de los Solari marchó hacia el campo <br>
 de los Rakkor junto a tres guerreros de armadura dorada y exigió detener el duelo. Los Solari adherían a una fe marcial<br>
 desarrollada alrededor del culto al sol, cuyos postulados implacables dictaban la vida en las cercanías y sobre el <br>
 Monte Targón. El anciano hacía llegado hasta los Rakkor por sueños y una profecía ancestral de los Solari que hablaba<br>
 de una guerrera cuyo fuego ardía más fuerte que el sol, una hija de Targón que traería la unidad al reino celestial. <br>
 El anciano creía que Leona era la elegida, y al conocer la naturaleza de su transgresión, el hecho no hizo más que <br>
 confirmar sus creencias.</P>

<br><P>Los videntes de la tribu le advirtieron sobre no interferir en el duelo, pero el anciano era inflexible; Leona <br>
debía ir con él y convertirse en uno de los Solari, y aprender sus enseñanzas. Los Rakkor eran muy independientes, pero<br>
 hasta ellos acataban los decretos sagrados de los Solari. Los guerreros sacaron a Leona de la arena y cargaron su <br>
 malogrado cuerpo lejos de los Rakkor, con destino hacia su nueva vida.</P>


<br><P>El templo de los Solari era una imponente ciudadela situada en las laderas orientales del Monte Targón, una brillante<br>
 torre de mármol veteado en oro y granito pulido. Allí, Leona aprendió las enseñanzas de la orden sagrada, cómo adoraban <br>
 al sol como una fuente de toda vida y rechazaban a cualquier otra forma de luz como algo falso. Sus enseñanzas eran absolutas<br>
 e inflexibles, pero impulsada por su creencia en la profecía del anciano, Leona sobresalió en este entorno disciplinado y <br>
 devoró los postulados de su nueva fe como si fuera un hombre sediento en el desierto en busca de agua que beber. Leona entrenó<br>
 día a día con la orden guerrera de los Solari, los Ra-Horak, un título rakkorano que significa Seguidores del Horizonte, <br>
 con quienes perfeccionó sus habilidades implacables con una espada hasta convertirlas en algo sublime. Con el tiempo, Leona <br>
 asumió el mando de los Ra-Horak, y llegó a ser conocida en los alrededores del Monte Targón como una justa, devota, y según <br>
 algunos, ferviente servidora del Sol.</P>

<br><P>Su camino cambió para siempre cuando la llamaron para escoltar a una joven miembro de los Solari hacia el corazón <br>
del templo. El cabello de la niña era de color blanco puro y una runa resplandeciente brillaba en su frente. Su nombre <br>
era Diana, una busca problemas que Leona conocía por las exasperadas desgracias relatadas por los ancianos del templo.<br>
 Diana había desaparecido hace meses, pero regresó vestida con una armadura pálida que destellaba un extraño fulgor <br>
 plateado. Diana afirmaba traer consigo grandes noticias, revelaciones que sacudirían a los Solari hasta sus cimientos, <br>
 pero que solo revelaría a los ancianos del templo.</P>

 
<br><P>Leona condujo a Diana hacia el templo con la guardia en alerta, ya que su instinto guerrero detectaba algo<br>
 extraño en la conducta de la muchacha. Presente ante los ancianos, Diana habló sobre los Lunari, una fe ancestral<br>
 y proscrita que veneraba a la luna, y afirmó que todas las verdades a las que se aferraban los Solari eran incompletas.<br>
 La muchacha describió un reino situado más allá de la cima de la montaña, un lugar en el que el sol y la luna no <br>
 eran enemigos, donde nuevas verdades podrían demostrarles nuevas maneras de ver el mundo. Leona sintió cómo crecía <br>
 su rabia con cada palabra que pronunciaba Diana, y cuando los ancianos rechazaron sus dichos y la llamaron blasfema,<br>
 Leona sabía que debía acabar con la vida de la hereje con su propia espada.</P>

<br><P>Leona observó cómo Diana enfurecía ante el rechazo de los ancianos, pero antes de poder reaccionar, la joven<br>
 de cabello blanco arremetió contra ellos. Una luz cegadora explotó desde las manos extendidas de Diana, y unos <br>
 orbes de fuego plateado incineraron a los ancianos hasta las cenizas en un abrir y cerrar de ojos. Un huracán <br>
 blanco de llamas y rayos helados impactó a Leona, lo que la arrojó lejos de la cámara. Cuando recuperó la conciencia,<br>
 descubrió que Diana había desaparecido y los Solari habían perdido sus líderes. Mientras los miembros restantes <br>
 trataban de comprender este ataque ocurrido en su lugar más sagrado, Leona sabía que solo le quedaba un camino por<br>
 recorrer. Ella perseguiría y destruiría a la hereje por el asesinato de los ancianos de los Solari.</P>

<br><P>Le resultó muy fácil rastrear el camino seguido por Diana. Las huellas de la hereje eran como mercurio <br>
reluciente a los ojos de Leona, y estos la condujeron hacia las altas laderas del Monte Targón. Leona no vaciló,<br>
 y ascendió por un paisaje que le era extraño y desconocido, como si caminara por senderos que no habían existido<br>
 sino hasta ese momento. El sol y la luna atravesaron el cielo en un abrir y cerrar de ojos, como si hubiesen pasado<br>
 muchos días y noches con cada aliento que daba. No se detuvo para beber ni comer, y se mantuvo en pie por su propia<br>
 furia, más allá de todo lo humanamente posible.</P>

<br><P>Finalmente, Leona alcanzó la cima de la montaña, exhausta, sin aliento, muerta de hambre y despojada de todo<br>
 pensamiento que no fuese castigar a Diana. Allí, sentado sobre una roca en la cima de la montaña estaba el mismo niño <br>
 de tez dorada cuya vida había perdonado cuando era joven. Detrás de él, el cielo ardía con una luz incandescente, <br>
 una aurora de colores increíbles y la sugerencia de una ciudad majestuosa de oro y plata. En sus acanaladas torres<br>
 y relucientes alminares, Leona pudo ver cómo el templo de los Solari reflejaba su magnificencia y cayó de rodillas <br>
 en trance.</P>

<br><P>El niño de tez dorada le habló en la antigua lengua de los Rakkor, y le dijo que estaba esperando que ella lo <br>
siguiera desde ese día, y que esperaba que no fuese demasiado tarde. Estiró su mano y le ofreció mostrarle los milagros<br>
 y conocer la mente de los dioses.</P>

<br><P>Leona nunca había rechazado nada en toda su vida. Tomó la mano del niño mientras este le sonreía y la conducía <br>
hacia la luz. Una columna de abrasadora iluminación impactó a Leona desde los cielos y quedó sumergida en ella. Sintió <br>
cómo una presencia impresionante recorría por su cuerpo con un poder terrorífico y el conocimiento olvidado de las <br>
primeras eras del mundo. Su armadura y sus armas ardieron hasta las cenizas en el fuego cósmico y renacieron como una <br>
placa de guerra adornada, un escudo de luz solar forjado en oro y una espada de amanecer encadenado.</P>

<br><P>La guerrera que descendió la montaña parecía ser la misma que había ascendido, pero Leona había cambiado mucho <br>
en su interior. Aún conservaba sus recuerdos y pensamientos, aún era dueña de su propio cuerpo, pero una entidad vasta <br>
e inhumana la había elegido para ser su recipiente mortal. La dotó de poderes increíbles y un terrible conocimiento <br>
que acecharía sus ojos y le pesaría mucho en el alma; un conocimiento que solo podría compartir con una persona.</P>

<br><P>Ahora, más que nunca, Leona sabía que debía encontrar a Diana.</P>
