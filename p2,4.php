<h2>Ekko</h2>
<h3>Historia</h3>
<br><P>Ekko, un prodigio surgido de las duras calles de Zaun, manipula el tiempo para su propio beneficio. Usando su propio invento, el Pulsar-Z, <br>
Ekko explora las diferentes posibilidades de la realidad para crear el momento perfecto. Aunque disfruta su libertad, si algo amenaza a <br>
sus amigos hará lo que sea para defenderlos. Para los forasteros, Ekko logra lo imposible a la primera, siempre.</p>

<br><P>Poseedor de un intelecto fenomenal, Ekko construía máquinas simples incluso antes de que pudiera gatear. Asombrados con estas muestras<br>
 de genialidad,sus padres, Inna y Wyeth, prometieron darle un buen futuro a su hijo. En su mente, Zaun, con su contaminación y crimen, no era<br>
 lugar para un niño con su genialidad. Trabajaron duras jornadas en una fábrica, bajo condiciones peligrosas, para forjar un camino de <br>
 oportunidades para su hijo en Piltóver.</p>

<br><P>Pero Ekko veía las cosas de manera diferente.</p>

<br><P>Vio cómo sus padres envejecían prematuramente, intentando que sus bajos salarios rindieran, mientras que su mercancía hecha a mano<br>
 era vendida a la gente adinerada de Piltóver por cantidades exorbitantes, ganancias que nunca vieron gracias a los supervisores codiciosos<br>
 de la Atarazana y sus astutos compradores. Los piltillos recorrían el Malecón en busca de experiencias buenas y baratas, o acudían al <br>
 Entresol para darse el gusto en clubes del tipo ''vale todo''. No, Ekko no compartía la visión que tenían sus padres de él viviendo una buena<br>
 vida en la privilegiada Ciudad del Progreso.</p>

<br><P>Por otro lado, Zaun... Donde sus padres solo veían las opresivas capas de asfixiante contaminación y los altos índices de criminalidad,<br>
 Ekko veía más allá y descubría una ciudad dinámica que desbordaba energía y potencial. Era un semillero de innovación pura, un crisol de <br>
 culturas remotas, inmigrantes unidos por el deseo de ser pioneros del futuro. Pero ni siquiera ellos estaban a la altura de los zaunitas <br>
 nativos. No las pandillas amplificadas con tecnología ni la escoria que encabezaba los periódicos de Piltóver por sus fechorías; sino los<br>
 chatarreros del sumidero, los ladrones químicos, los horticultores que cuidaban los cultivaires. Estos, y muchos otros más, eran el corazón<br>
 y el alma de la ciudad. Eran capaces, resistentes, y laboriosos. Ellos construyeron una cultura próspera a partir de la catástrofe y florecieron<br>
 donde otros hubieran perecido. El espíritu de Zaun cautivó a Ekko, lo llevó a construir sus máquinas exclusivamente con los desperdicios <br>
 que nadie más valoraba y lo empujó a probarlas en sí mismo.</p>

<br><P>No era el único que poseía ese espíritu. Ekko se hizo amigo de huérfanos pobres, de fugitivos inquisitivos y de cualquiera cuya sed por<br>
 la emoción fuera tan contagiosa como la viruela gris. Todos tenían talentos únicos: desde escalar hasta esculpir, desde pintar hasta diseñar.<br>
 Muchos zaunitas descartaban la idea de una educación formal en favor de convertirse en aprendices de oficios. Los autoproclamados Niños<br>
 Perdidos de Zaun concebían las laberínticas calles de Zaun como mentoras y gastaban todo su tiempo de manera gloriosa y juvenil. Se <br>
 retaban a correr carreras a pie por los Mercados de los Linderos. Se desafiaban a trepar las precarias rutas del Sumidero al Entresol y<br>
 hacia el Malecón. Corrían libres y salvajes, respondiendo únicamente a sus caprichos.</p>

<br><P>Para diferenciarse de las pandillas criminales y otros químico-punks, él y sus amigos optaron por no modificar sus cuerpos. La <br>
amplificación era, para ellos, una pérdida de dinero y motivo de desapruebo. La misma actitud sostenían con respecto a robarle a alguien<br>
que no tuviera nada o menos de lo que ellos poseían. Esto hizo que los piltillos de clase alta y los matones con mejoras tecnológicas se <br>
convirtieran en atractivos blancos para sus travesuras. Decoraron sus escondites secretos con bienes hurtados y obras de arte pintadas<br>
directamente sobre las paredes. Los Niños Perdidos de Zaun se sentían invencibles.</p>

<br><P>Al crecer, los inventos de Ekko se tornaron cada vez más fantásticos y complejos, y requerían de componentes exóticos que necesitaban<br>
 ser ''liberados''de los tiraderos. Lo bueno era que tenía a una visión bastante flexible sobre la propiedad privada. Pronto, los matones <br>
 viginautas tecnológicos y los inquietantemente agresivos guardias de seguridad estaban al acecho constante de Ekko y su tropa de renegados<br>
 y,con frecuencia, perseguían a los adolescentes. Siempre le divirtió ver cómo los laboratorios de Piltóver y las fábricas de los barones<br>
 químicos resguardaban ferozmente su basura. Ni que usaran la chatarra tecnológica para algo. Él, por otra parte, podía reutilizar <br>
 su basura con un poco de ingenio.</p>

<br><P>Una noche, mientras Ekko recorría las ruinas de un laboratorio demolido hace poco, hizo un descubrimiento sorprendente:<br>
 una esquirla de una gema azul marina que brillaba con energía mágica. Buscó más y encontró otros fragmentos de la gema brillante.<br>
 Las esquirlas zumbaban como si estuvieran tratando de cantar una melodía rota, la canción se magnificaba cuando estas se encontraban más<br>
 cerca. Con mucho cuidado, buscó cada del cristal roto, a pesar de que algunas se encontraban enterradas debajo de toneladas de escombros,<br>
 para lo cual tuvo que escabullirse entre trozos de desechos apestosos. Todos los niños de Zaun habían escuchado historias sobre los cristales<br>
 hextech. Daban energía a armas y héroes. Podían crear energía solos. Los cristales hextech tenían el potencial para cambiar el mundo. Ahora<br>
 él sostenía uno roto.</p>

<br><P>Antes de que pudiera celebrar su hallazgo, se percató de que el lugar estaba plagado de viginautas que revisaban las ruinas, en búsqueda<br>
 de algo. Ekko sabía que buscaban las piezas del cristal que tenía en la mano. Apenas logró escapar sin que lo detectaran.</p>

<br><P>Después de un estudio meticuloso, Ekko se dio cuenta de que los rastros débiles de energía aumentaban cuando los cristales se agrupaban;<br>
 sus bordes crepitaban y emitían olas de distorsión ondulante por el aire. Cuando separó las piezas, una resistencia casi magnética peleó contra <br>
 sus esfuerzos. Era como si los cristales astillados recordaran ser uno solo. Aún más curioso, Ekko tuvo una sensación muy extraña; un <br>
 inquietante sentimiento de recordar un momento, solo que un poco diferente.</p>

<br><P>Sus manos no podían seguir el ritmo de las ideas que su mente tenían para el cristal. Durante uno de sus experimentos menos científicos,<br>
 la gema explotó en un vórtice de polvo brillante y detonó remolinos de distorsión temporal. Ekko abrió los ojos y vio distintas realidades <br>
 astilladas (además de algunas versiones ''eco'' de sí mismo) mirándolo en pánico en medio del continuo fracturado.</p>

<br><P>Esta vez sí que se había pasado.</p>

