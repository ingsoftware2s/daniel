<br><P>Después de un momento de tensa coordinación entre Ekko y sus paradojas, lograron contener y reparar el difícil hoyo que <br>
había desgarrado del tejido de la realidad.</p>

<br><P>Con el tiempo, Ekko encauzó los poderes temporales del cristal roto para crear un dispositivo que le permitiera manipular pequeños lapsos <br>
temporales, bueno, al menos en teoría. Antes de que pudiera probar su última máquina, sus amigos lo presionaron para escalar el <br>
Viejo Cascarrabias el día de su santo, por lo que Ekko colgó el dispositivo de su hombro y lo llevó consigo.</p>

<br><P>Caminaron hasta la vieja torre del reloj en el corazón de la Antigua Zaun y escalaron, deteniéndose esporádicamente para dibujar<br>
 caricaturas obscenas de uno o dos piltillos prominentes. Ya estaban cerca de la cima cuando uno de los asideros se desprendió e hizo que<br>
 uno de sus amigos se resbalara y cayera de la torre. De manera instintiva, como si lo hubiera hecho mil veces antes, Ekko activó el <br>
 dispositivo que contenía el cristal. El mundo a su alrededor se hizo trizas y él fue succionado hacia remolinos de partículas de tiempo.</p>

<br><P>El vello de sus brazos se estremecía con electricidad. Un extraño mareo nublaba su mente. Entonces miró a su amigo acercarse a la <br>
viga podrida para repetir su fatal error. ¡CRAC! La viga cedió al peso del chico, pero Ekko se estiró y agarró a su amigo por el cuello <br>
de la camiseta y lo empujó a una cornisa próxima. Por desgracia, erró en la trayectoria y lanzó a su amigo hacia los afilados engranajes<br>
 de la torre del reloj. Ups.</p>

<br><P>Tras numerosos retornos y algunos ajustes de la gradiente del viento, Ekko salvó la vida de su amigo. Para los otros, parecía <br>
que Ekko contaba con los reflejos de un dios. De manera instantánea su estatus se elevó. Les dijo acerca del cristal y de la manipulación<br> 
del tiempo y les hizo jurar mantener el secreto. Pero en vez de hacerlo, sin vergüenza alguna, exageraron las hazañas de su amigo y se<br>
 retaron a intentar trucos cada vez más imprudentes, sabiendo que estarían a salvo. Con cada prueba (y muchos errores) la máquina del <br>
 tiempo,que llamó Pulsar-Z, se volvió cada vez más estable. Ekko descubrió que podía hurtar componentes, azotar a matones químico-punks <br>
 imponentes e incluso atinar las frases románticas correctas, logrando una buena primera impresión todas las veces. El único límite <br>
 se encontraba en cuánto podría soportar su cuerpo soportar de que lo colmara el cansancio.</p>

<br><P>Los rumores e historias de las travesuras de los viajes en el tiempo de Ekko llegaron a los oídos de algunas personas poderosas de<br>
 las dos ciudades.Viktor, un científico zaunita muy respetado (y temido), tenía un profundo interés por acordar una audiencia con este <br>
 genio desafiante y equipó a varios de sus agentes de bajo mando con poderosas mejoras para alentar al chico a que se uniera a sus servicios.<br>
 Por su parte, Jayce, el renombrado innovador de Piltóver, estaba ansioso por evaluar al joven que fragmentó el tiempo y aplicar a su tecnología<br>
 la ingeniería inversa. No obstante, Ekko valora demasiado su independencia y no tiene ningún deseo de ser parte de los planes de nadie. <br>
 Unos cuantos acosadores podrían obtener un vistazo de Ekko antes de ser frustrados, a menudo de forma vergonzosa, por el rufián del <br>
 sumidero con el don preternatural de señalar su debilidad exacta.</p>

<br><P>En sus sueños más atrevidos, Ekko imagina que su ciudad natal se elevaba para eclipsar a la Ciudad del Progreso. El enchapado dorado<br>
 de Piltóver sería opacado por el ingenioso brillo e incesante coraje de una Zaun nacida no de generaciones de privilegio, sino de la audacia.<br>
 Tal vez aún no tenga un plan, pero Ekko tiene todo el tiempo del mundo para hacer de su sueño una realidad.</p>

<br><P>Después de todo, si puede cambiar el pasado, ¿qué tal difícil será cambiar el futuro?</p><br>

