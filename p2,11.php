<br><P>Una mañana, mientras se ocupaba de sus quehaceres en el cementerio, lo interrumpió la aparición de una nube negra como el carbón<br>
 que avanzaba sobre las Islas Bendecidas, devorándolo todo a su paso. Trató de escapar, pero la nube lo alcanzó rápidamente y lo <br>
 sumió en la oscuridad.</P>

<br><P>A su alrededor, todos los seres vivos empezaron a contorsionarse, corrompidos por la impía magia de la Niebla Negra. Las personas,<br>
 los animales e incluso las plantas se transformaban en siniestras y viles caricaturas de su antiguo yo. El aire turbulento que los <br>
 rodeaba estaba cuajado de susurros y sus hermanos comenzaron a arrancarse los frascos de agua curativa del cuello, como si les causaran <br>
 una profunda angustia. Un instante después, Yorick contempló con horror cómo las almas de los monjes eran arrancadas de sus cuerpos, <br>
 sin dejar más que fríos y pálidos cadáveres tras de sí.</P>

<br><P>Entre los gritos cada vez más débiles de sus hermanos, solo Yorick podía oír las veces que sonaban en la niebla:</P><br>

<br><P>“Quítatelo. Únete a nosotros. Seremos uno”.</P>

<br><P>Sintió que sus dedos buscaban el frasco que llevaba al cuello. Haciendo acopio de toda su determinación, apartó las manos de la<br>
 garganta y ordenó a las almas que dejaran de aullar. La Niebla Negra se estremeció con violencia y la oscuridad se tragó a Yorick.</P>

<br><P>Al despertar, los vientos habían cesado y la tierra antaño fértil se había transformado en el grotesco yermo de las Islas de la Sombra.<br>
 Unos solitarios zarcillos de Niebla Negra se aferraban aún a él, como si quisieran apoderarse del único ser vivo al que aún no habían <br>
 logrado corromper. Pero, al tiempo que la Niebla se ensortijaba a su alrededor, Yorick se dio cuenta de que el frasco de su cuello la <br>
 repelía. Sus manos lo agarraron, al comprender que era lo único que lo mantenía con vida.</P>

<br><P>Durante los días siguientes recorrió la isla en busca de supervivientes, pero no encontró otra cosa que los retorcidos restos de <br>
sus antiguos habitantes. Por todas partes, lo único que podía ver eran los espíritus desdichados que se levantaban de los cadáveres.</P>

<br><P>Mientras buscaba, poco a poco, empezó a juntar las piezas de los sucesos que habían desembocado en aquel cataclismo: un rey había<br>
 arribado a la isla para resucitar a su reina, pero lo único que había conseguido había sido condenar al archipiélago, con todos sus habitantes.</P>

<br><P>Yorick habría querido partir en busca de aquel ''rey arruinado'', para deshacer la maldición que había provocado. Pero se sentía<br>
 impotente frente a la infinidad de muerte que lo rodeaba.</P>

<br><P>Casi perdido en su tristeza, comenzó a hablar con los espíritus que había a su alrededor, tratando de encontrar el mismo consuelo<br>
 que de niño. Pero mientras se comunicaba con la Niebla, los cadáveres empezaron a salir de sus tumbas, atraídos por su voz. Entonces <br>
 comprendió que los cuerpos que había enterrado en su día estaban ahora bajo su mando.</P>

<br><P>Un destello de esperanza se encendió en su corazón sumido en la desesperación. Para liberar las Islas de la Sombra, utilizaría <br>
su poder y sus fuerzas.</P>

<br><P>Para acabar con la maldición, se vería obligado a utilizarla.</P>
