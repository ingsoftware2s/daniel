<h2>Yorick</h2>
<h3>Historia</h3>
<br><P>Yorick, último superviviente de una orden religiosa olvidada hace tiempo, carga con la bendición y la maldición<br>
 del poder sobre los muertos. Atrapado en las Islas de la Sombra, sus únicos compañeros son cadáveres en descomposición<br>
 y los espíritus aullantes que atrae. Sus monstruosos actos ocultan un objetivo noble: liberar su hogar de la maldición <br>
 de la Ruina.</P>

<br><P>Yorick no llevó una vida normal ni siquiera de niño. Criado en una aldea pesquera en un extremo de las Islas Bendecidas,<br>
 siempre tuvo que luchar para ser aceptado. Mientras la mayoría de los niños de su edad jugaban al escondite, él hacía amigos<br>
 de otro tipo: los espíritus de los que acababan de morir.</P>

<br><P>Al principio, esta capacidad de ver y oír a los muertos lo aterraba. Siempre que alguien moría en la aldea, Yorick pasaba<br>
 la noche en vela, esperando el aterrador llanto de un nuevo visitante. No podía entender por qué lo atormentaban, ni por qué sus<br>
 padres creían que los espíritus eran solo pesadillas.</P>

<br><P>Con el tiempo, empezó a darse cuenta de que aquellos seres no pretendían hacerle ningún mal. Solo estaban perdidos y <br>
necesitaban ayuda para encontrar el camino al más allá. Y como solo él parecía capaz de verlos, decidió convertirse en su guía<br>
 y escoltarlos hasta lo que quiera que los esperase en la eternidad.</P>

<br><P>Era una tarea agridulce. Yorick disfrutaba de la compañía de los fantasmas, pero cada uno al que daba descanso era un <br>
nuevo amigo del que tenía que despedirse. Para los muertos era un salvador, pero para los vivos, un paria. Los aldeanos solo <br>
veían a un niño perturbado que hablaba con gente que no estaba allí.</P>

<br><P>Las historias sobre sus visiones no tardaron en propagarse más allá de la aldea, hasta llamar la atención de una pequeña<br>
 orden de monjes de las Islas Bendecidas. Sus enviados viajaron hasta la isla de Yorick, convencidos de que podía convertirse en<br>
 una herramienta de su fe.</P>

<br><P>Yorick accedió a acompañarlos hasta su monasterio, donde aprendió los caminos de los Frailes del Crepúsculo y el verdadero<br>
 significado de sus símbolos. Cada monje llevaba consigo una pala, símbolo de su deber de realizar los ritos funerarios que<br>
 garantizaban que no se extraviaran las almas, así como un frasquito de agua procedente del sagrado manantial de sus islas. <br>
 Este ''Aliento de Vida'' representaba el deber de los monjes de curar a los vivos.</P>

<br><P>Pero, por más que lo intentó, Yorick nunca logró ganarse la aceptación de los demás monjes. Para ellos, era la prueba tangible<br>
 de cosas que solo debían conocerse a través de la fe. Envidiaban su capacidad de percibir sin esfuerzo lo que ellos solo empezaban<br>
 a comprender tras una vida entera dedicada a su estudio. Despreciado por sus hermanos, volvió a encontrarse solo.</P>
