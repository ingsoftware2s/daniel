<h2>Volibear</h2>
<h3>Historia</h3>
<br><P>Las inhóspitas regiones al norte del Fréljord son el hogar de los ursinos, una feroz raza de guerreros que<br>
 ha sobrevivido en la tundra durante miles de años. Su líder es un feroz adversario que posee la fuerza del rayo<br>
 para sembrar el pánico entre sus enemigos: Volibear. Tanto guerrero como místico, Volibear desea defender las <br>
 antiguas tradiciones y el espíritu de guerreros de su tribu.</P>

<br><P>Aunque la historia recuerda sus legendarias hazañas en combate del pasado, ahora los ursinos vivían en un<br>
 tranquilo aislamiento. Los guerreros estaban bajo las órdenes de un triunvirato de líderes que mantenían un <br>
 longevo aislamiento, evitando los pequeños conflictos y asuntos de los demás. Volibear era el chamán de los <br>
 tres, un sabio respetado por su conocimiento. Era una época de paz sin precedentes, pero Volibear sentía en <br>
 su interior que algo no estaba bien. La prosperidad estaba provocando que su tribu fuese débil, y muchos <br>
 habían olvidado ya hace mucho el sagrado arte de la guerra. Volibear sentía que, con el tiempo, el fuego de <br>
 sus almas se extinguiría. Cuando reveló sus inquietudes al triunvirato, estos se negaron a escuchar y le <br>
 advirtieron que no olvidase cuál era su posición.</P>
 
