<br><P>En busca de sabiduría, Volibear inició un peligroso viaje a la cima de la montaña sagrada de los ursinos,<br> 
un lugar siempre envuelto por una tormenta eléctrica. Se decía que el ojo de la tormenta otorgaba augurios, y la <br>
leyenda afirmaba que la tempestad determinaría quién sería el próximo gran jefe de la tribu. Mientras ascendía a <br>
la cima, un rayo para nada natural cayó sobre él. Cuando el chamán despertó, estaba poseído por una terrible visión<br>
 del Fréljord, completamente consumida por la oscuridad. Volibear vio cómo una fuerza ursina, nada preparada y <br>
 condescendiente, era masacrada por terribles criaturas de hielo. Al instante, supo que su raza perecería si no <br>
 se preparaban para la guerra.</P>

<br><P>Volibear bajó a toda prisa de la montaña, para contar lo que había visto, pero tres ursinos le bloqueaban <br>
el paso: el triunvirato. Sabiendo que pondría fin a la duradera paz, se negaron a escuchar la advertencia de Volibear <br>
y exigieron su silencio, ya fuese por propia voluntad o por muerte. Volibear, decidido y tenaz, juró que la propia <br>
supervivencia de los ursinos dependía de su mensaje, por lo que se enzarzó en un intenso combate contra los tres. El<br>
 enfrentamiento fue terrible, y en cuanto Volibear sucumbió ante sus oponentes, apeló al poder de la tormenta. Liberando<br>
 un poderoso rayo, asestó al trío un golpe terrible. Aturdido y atontado, el triunvirato observó la señal del liderazgo <br>
 de los ursinos: el poder de la tormenta sagrada.</P>

<br><P>Respetando la profecía, el triunvirato nombró a Volibear nuevo líder de los ursinos. Su influencia se expandió<br>
 rápidamente y de forma decisiva: sacó a su tribu de la autocomplacencia, revivió sus tradiciones de batalla y se alió<br>
 con Sejuani, la guerrera que lucharía con ellos contra el mal que los acechaba. Con el tiempo, la tribu se volvería <br>
 más fuerte y feroz, y se expandiría una vez más su fama de terribles guerreros de leyenda. Ahora, Volibear y los ursinos<br>
 están preparados para los días oscuros que acechan en el horizonte helado.</P>

