<h2>Garen</h2>
<h3>Historia</h3>

<br><P>Nacido en la noble familia Guardia de la Corona, junto con su hermana menor Lux, Garen supo desde temprana edad<br>
 que se esperaba que él defendiera el trono de Demacia con su vida. Su padre, Pieter, era un oficial militar condecorado,<br>
 mientras que su tía Tianna era la Capitana de la espada de la Vanguardia Valerosa, una fuerza militar de élite; ambos <br>
 fueron reconocidos y muy respetados por el rey Jarvan III. Por lo tanto, se daba por hecho que, con el correr del tiempo,<br>
 Garen le serviría al hijo del rey de la misma manera.</P>

<br><P>El reino de Demacia se había levantado de las cenizas de las Guerras Rúnicas, y los siglos subsecuentes estuvieron<br>
 plagados de conflictos y de luchas. Uno de los tíos de Garen, un caballero guardián del ejército demaciano, solía contarles<br>
 a los pequeños Garen y Lux historias sobre sus aventuras del otro lado de las murallas del reino para proteger a su gente<br>
 de los peligros del mundo que estaba más allá.</P>

<br><P>Les advirtió que, sin duda, un día algo le pondría fin a ese tiempo de aparente paz; ya fueran magos rebeldes, criaturas<br>
 del abismo u otros horrores inimaginables por venir.</P>

<br><P>Como confirmación de esos temores, fue asesinado por un mago en el cumplimiento de su deber, antes de que Garen <br>
cumpliera once años. Garen presenció el dolor que esto trajo a su familia, así como el miedo en los ojos de su hermana menor.<br>
 En ese momento, supo con certeza que la magia era el principal y el más grande peligro que Demacia enfrentaba, por lo que<br>
 juró que no la dejaría traspasar sus murallas. El reino podría mantenerse a salvo solo si se seguían sus ideales fundacionales<br>
 y se desplegaba su sólido orgullo.</P>

<br><P>Cuando cumplió doce años, Garen dejó el hogar familiar de Guardia de la Corona en Lago Plateado para unirse al ejército.<br>
 Como escudero, sus días y sus noches se consumieron en el entrenamiento y el estudio de la guerra, perfeccionando cuerpo y mente<br>
 para que se convirtieran en un arma tan poderosa y verdadera como el acero demaciano. Fue entonces cuando conoció al joven <br>
 Jarvan IV, el príncipe al que, como rey, serviría algún día; él se encontraba entre los otros reclutas y se volvieron inseparables.</P>
 
