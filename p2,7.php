<br><P>En los años siguientes, Garen se ganó su lugar en la barrera de escudos como un guerrero de Demacia y pronto adquirió una <br>
reputación temeraria en el campo de batalla. Cuando cumplió dieciocho años, ya había servido con honor en campañas a lo largo de <br>
las fronteras freljordianas, protagonizado un papel clave al purgar de fétidos sectarios el Bosque del Silencio, y peleado junto <br>
con los valientes defensores de Roca Blanca.</P>

<br><P>El mismo rey Jarvan III había convocado al batallón de Garen para que regresara a la Gran Ciudad de Demacia, honrándolos <br>
ante la corte real en el Salón del Valor. Tianna Guardia de la Corona, recientemente elevada al cargo de Mariscal superior, destacó<br>
 en particular a su sobrino, y lo recomendó para que hiciera todas las pruebas necesarias para unirse a las filas de la Vanguardia<br>
 Valerosa.</P>

<br><P>Garen volvió a casa para prepararse, en donde Lux y sus padres lo recibieron cálidamente, así como la gente sencilla que <br>
vivía en la propiedad de la familia. A pesar de que se sentía satisfecho de que su hermana estuviera convirtiéndose en una joven <br>
mujer inteligente y capaz, algo en ella había cambiado. Cada vez que iba de visita se percataba de ello, pero ahora Garen batallaba<br>
 con una sospecha real y persistente de que Lux tenía poderes mágicos. De cualquier manera, nunca pensó en la idea por mucho tiempo. <br>
 Pensar que un Guardia de la Corona tuviera los mismos poderes prohibidos que mataron a su tío era una idea demasiado descabellada.</P>

<br><P>Como era de esperarse, por medio de la valentía y el talento, Garen se ganó su lugar en la Vanguardia. Frente a los ojos de <br>
su orgullosa familia y su buen amigo el príncipe, prestó juramento ante el trono.</P>

<br><P>Lux y su madre pasaron mucho más tiempo en la capital, sirviéndole al rey, así como a la humilde orden de los Iluminadores; <br>
no obstante, Garen trató de mantener su distancia tanto como fuera posible. Si bien amaba a su hermana más que a nada en el mundo,<br>
 a una pequeña parte de él le costaba acercarse a ella. Trató de no pensar en lo que estaría obligado a hacer en caso de que sus <br>
 sospechas llegaran a confirmarse. En cambio, se dedicó por completo a sus nuevos deberes, peleando y entrenando con una intensidad<br>
 redoblada.</P>

<br><P>Cuando el nuevo Capitán de la espada de la Vanguardia Valerosa cayó en la batalla, Garen fue propuesto por sus compañeros <br>
guerreros para asumir el mando; su nominación no tuvo opositores.</P>

<br><P>Hasta hoy, permanece firme en la defensa de su tierra natal contra todos los enemigos. Más allá de ser el soldado más formidable<br>
 de Demacia, él es la encarnación de los ideales más grandes y nobles sobre la cual fue fundada.</P>
