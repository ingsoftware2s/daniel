<br><P>Tras la victoria devastadora contra las fortaleza aéreas de los Varju, un orgulloso pueblo guerrero que <br>
había resistido durante décadas a la invasión noxiana, Darius fue nombrado ''la Mano de Noxus'' por el mismísimo <br>
emperador Boram Darkwill. Aquellos que conocían bien a Darius sabían que no buscaba poder ni adulación: su mayor <br>
deseo era ver a Noxus triunfar sobre todo. Es por ello que Darkwill le ordenó dirigirse con su batallón hacia el <br>
norte lejano hasta Fréljord, para someter definitivamente a las tribus bárbaras.</P>

<br><P>La campaña continuó por varios años hasta llegar a un amargo y helado punto muerto. Darius apenas sobrevivió a <br>
los intentos de asesinato, las emboscadas e incluso la captura por la despiadada Garra Invernal. Comenzaba a cansarse<br>
 de las interminables guerras de desgaste, así que regresó a Noxus para exigir la reconsolidación de la milicia.</P>

<br><P>Marchó junto a sus veteranos a la capital, solo para descubrir que el emperador había sido asesinado en un <br>
golpe de estado liderado por Jericho Swain. El acto había sido apoyado por muchos aliados, incluyendo al hermano de<br>
 Darius, Draven.</P>

<br><P>Su dilema era complejo. Al ser la Mano de Noxus, muchas casas señoriales esperaban que Darius vengara a Darkwill,<br>
 pero conocía y respetaba profundamente al desprestigiado general Swain y se había opuesto a su despido tras la ofensiva <br>
 fallida que había tomado lugar en Jonia unos años atrás. La lealtad de la Mano era hacia Noxus, no hacia algún líder en<br>
 específico, y Swain era un hombre que expresaba con honestidad su nueva visión del imperio. Darius decidió que era un <br>
 líder al que estaba preparado para seguir... pero Swain tenía algo distinto en mente.</P>

<br><P>Con el establecimiento de la Trifarix, tres individuos gobernarían juntos a Noxus, y cada uno personificaría un <br>
principio de fuerza: Visión, Poder y Astucia. Darius aceptó con gusto su lugar en el consejo y prometió erigir una nueva<br>
 fuerza de élite, la Legión Trifariana, conformada por los guerreros más leales y prestigiosos que un imperio podría formar,<br>
 y así guiar a los ejércitos noxianos hacia una nueva era de conquista gloriosa.</P>
