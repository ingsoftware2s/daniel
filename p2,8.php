<h2>Darius</h2>
<h3>Historia</h3>

<br><P>Tras quedar huérfanos, Darius y su hermano Draven crecieron en la ciudad portuaria de Basilich. Darius <br>
se esforzaba por ser el proveedor de ambos, mientras peleaba contra pandillas de jovencitos y con quienquiera<br>
 se atreviera a amenazar a su hermano pequeño, aunque se tratara de la guardia de la ciudad. Cada día en las <br>
 calles era una verdadera lucha por la supervivencia, y Darius obtuvo más cicatrices en su doceavo verano que<br>
 las que llegan a tener algunos soldados durante toda su vida.</P>

<br><P>Tras la captura de Basilich por el imperio noxiano en plena expansión, el victorioso comandante Cyrus <br>
reconoció la fuerza de los hermanos desafiantes y les proporcionó un hogar entre las filas de su batallón. En <br>
el transcurso de los años pelearon en extenuantes campañas de conquista de un extremo del mundo al otro y <br>
reprimieron numerosas rebeliones contra la corona.</P>

<br><P>Cualquiera podía ascender al poder en el imperio, sin importar su origen, cultura o historia, y nadie <br>
acogía este ideal de forma tan ferviente como lo hacía Darius. De inicios humildes, fue ascendiendo de rango de<br>
 manera constante, siempre priorizando el deber ante todo, y fue así como se ganó el respeto de los demás por <br>
 su agresividad, disciplina y su determinación hacia nunca retroceder. Incluso decapitó a un general noxiano <br>
 cuando el cobarde ordenó la retirada en los ensangrentados campos de la Planicie de Dalamor. Darius movilizó <br>
 a los guerreros con rugidos desafiantes y agitando su hacha ensangrentada sobre la cabeza, consiguiendo una <br>
 grandiosa e inesperada victoria contra un enemigo que los superaba en número.</P>

<br><P>Como recompensa obtuvo un cargo de alto mando, lo que atrajo a cientos de reclutas deseosos provenientes<br>
 de todo el imperio. Darius rechazó a la mayoría y solo aceptó a los guerreros más fuertes, disciplinados y con<br>
 una voluntad de acero. Su reputación era tan imponente que, incluso en los territorios más allá de Noxus, no era<br>
 raro que ciudades enteras se rindieran al primer avistamiento de sus estandartes.</P>
 
