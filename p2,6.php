<br><P>Sin embargo, la venganza no fue lo que la estepa le brindó. Había sobrevivientes, y no lograrían permanecer mucho más<br>
 tiempo en el mundo si Tryndamere no conseguía quién los refugiara. Hacia el sur estaban los noxianos, al norte la Guardia <br>
Helada, y la oscura figura había venido desde el este. Al oeste, se decía que algunas tribus se estaban reuniendo para la <br>
 supuesta reencarnación de Avarosa. En el pasado, él habría descartado esos rumores tan fantásticos, pero en ese momento sabía<br>
 que esa era su única opción.</P>

<br><P>Tryndamere y los sobrevivientes de su clan llegaron al valle como poco más que mendigos. El joven guerrero estaba <br>
determinado a demostrar el valor de su clan, y así conseguir la protección del líder avarosano para poder regresar a cobrar su venganza.<br>
 Blandiendo su espada con forma de colmillo,hizo lo que le era natural y retó a los demás a duelos. Teniendo en su mente la imagen<br>
 de la oscura criatura y el eco de su risa, Tryndamere derrotó con rapidez a todos los que se atrevieron a enfrentarlo.</P>

<br><P>Su ira singular era profundamente inquietante para los avarosanos. Los guerreros del norte también se percataron de <br>
