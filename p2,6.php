 furia, más rápido sanaba su cuerpo. Muchos sospechaban que él y su clan practicaban magia extraña y antinatural, por lo que el plan de <br>
 Tryndamere de demostrar el valor de su gente comenzaba a hacer que peligrara la aceptación hacia su gente.</P>

<br><P>Pero no todos los avarosanos se habían puesto en su contra. Ashe, su matriarca, buscaba fortalecer su posición con un <br>
matrimonio político con alguien que pudiera hacerle frente a los interminables retadores que pretendían ganarse su mano y <br>
su mandato. Viendo una oportunidad en el atractivo bárbaro, ella juró acoger a su clan como avarosanos si Tryndamere se <br>
convertía en su primer y único Jurasangre.</P>

<br><P>A medida que pasaba tiempo en compañía de Ashe, Tryndamere comenzó a creer en los rumores: ciertamente ella era la <br>
reencarnación divina de Avarosa. Gracias al liderazgo mesurado de Ashe, la furia de Tryndamere se convirtió en templanza,<br>
 y un auténtico afecto surgió entre ambos.</P>

<br><P>Aun así, en su calidad de campeón de Ashe, ahora Tryndamere se enfrenta a un futuro incierto. El rey bárbaro puede <br>
ver claramente en el horizonte del Fréljord una guerra que se avecina, pero su sed de venganza personal sigue allí y <br>
comienza a preguntarse si tal vez su suerte predestinada podría no ser junto a su reina.</p>

