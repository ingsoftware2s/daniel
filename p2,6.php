<h2>Tryndamere</h2>
<h3>Historia</h3>

<br><P>Tryndamere llegó al mundo conociendo solo la dureza de la supervivencia en las eternas estepas congeladas donde<br>
 su clan hizo su hogar. A pesar de que alababan a todos los antiguos dioses del Fréljord, al igual que al Culto de<br>
 las Tres, le rezaban con mayor frecuencia a una deidadespiritual conocida por devastar la tundra: un dios del <br>
 colmillo, vigoroso e inmortal. Dado que los materiales requeridos para hacer armaduras eran escasas, el clan<br>
 destinó sus recursos para forjar espadas majestuosas, inspiradas en los colmillos de marfil de su<br>
 dios.</P>

<br><P>La resistencia y la pericia en el combate del pueblo de Tryndamere se convirtió en legendaria. Tenían la capacidad<br>
 de defenderse de otras tribus saqueadoras, de asesinar a las grandes bestias de las montañas y de repeler a los noxianos que <br>
 invadían desde el sur. El mismo Tryndamere se volvió un guerrero temible e impetuoso, pero no fue sino hasta la noche de <br>
 un invierno particularmente cruel que puso a prueba su fuerza. Una tormenta inusual se extendió desde el este, atrayendo<br>
 consigo una oscuridad helada y a una silueta imponente y astada contra la luna llena.</P>

<br><P>Algunos miembros del clan se arrodillaron creyendo que su dios jabalí estaba ante ellos. Esta criatura rebosaba magia<br>
 antigua y auténtica, pero no pertenecía al Fréljord... y aquellos que se arrodillaron fueron los primeros en perder la vida.</P>

<br><P>Tryndamere observó horrorizado. Podía sentir una maldad brutal surgiendo en su corazón mientras era testigo de la<br>
 espada viviente y despiadada del invasor. Ya fuera por la sed de sangre o por alguna otra locura, Tryndamere alzó<br>
 su propia espada y dejó escapar un rugido desafiante.</P>

<br><P>La figura oscura lo hizo a un lado como si fuera un insecto.</P>

<br><P>Tryndamere cayó en la nieve empapada de color casi negro por la sangre, rodeado de muertos. Profirió lo que él <br>
pensó que serían sus últimas respiraciones mientras la criatura se acercaba y comenzaba a hablar. Tryndamere intentó <br>
aferrarse a las palabras extrañas y arcaicas,pero conforme la fuerza de su vida se desvanecía, fue la risa de la criatura<br>
 que penetró la memoria del joven guerrero para siempre.</P>
 
